
public class Room {

	int roomNumber;
	int roomType;
	
	public void setRoomNumber(int nroomNumber){
		roomNumber = nroomNumber;
	}
	
	public void setRoomType(int nroomType){
		roomType = nroomType;
	}
	
	public int getRoomType(){
		return roomType;
	}
	
	public int getRoomNumber(){
		return roomNumber;
	}
}
